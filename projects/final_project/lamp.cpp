Lamp::~Lamp(){
    cleanup();
}
void Lamp::cleanup(){
    //clean up
    if (VBO != 0) {
        glDeleteBuffers(1, &VBO);
        VBO = 0;
    }

    if (lightCubeVAO != 0) {
        glDeleteVertexArrays(1, &lightCubeVAO);
        lightCubeVAO = 0;
    }
}
void Lamp::initShader(){

    const std::string lightCubeVsFile("../../../projects/final_project/light_cube.vs");
    const std::string lightCubeFsFile("../../../projects/final_project/light_cube.fs");

    _lightCubeShader.reset(new Shader(lightCubeVsFile,lightCubeFsFile));

}
void Lamp::draw(const glm::mat4& projection, const glm::mat4& view){
    // draw the lamp object(s)
    glBindVertexArray(lightCubeVAO);
    _lightCubeShader->use();
    _lightCubeShader->setMat4("projection", projection);
    _lightCubeShader->setMat4("view", view);
    glm::mat4 model = glm::mat4(1.0f);
    // we now draw as many light bulbs as we have point lights.
    for (unsigned int i = 0; i < 1; i++)
    {
        
        model = glm::mat4(1.0f);
        model = glm::translate(model, _pointLightPositions[i]);
        model = glm::scale(model, glm::vec3(10.0f)); // Make it a larger cube
        _lightCubeShader->setMat4("model", model);
        glDrawArrays(GL_TRIANGLES, 0, 36);
    }
}